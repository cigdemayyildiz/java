package Loops;

public class WhilePractice2 {

    public static void main(String[] args) {
        int age = 23;
        while (age<=26){
            System.out.println("I am here today");
            age++;
        }

        int number = 10;
        while (number<=20){
            System.out.println(number);
            number++;
        }
    }
}
