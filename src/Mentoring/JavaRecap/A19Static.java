package Mentoring.JavaRecap;

public class A19Static {

    // Possible Interview Questions:
    // What is static keyword?
    // Static keyword is a keyword to make the methods belongs to the class
    // the advantage of having static keyword is that you do not need to create an object to access the variables
    // or methods.
    // the disadvantage of having static keyword is that you cannot override the methods once you call it.
    // What is static block?
    // Static block is the block that can be called once (1 time it works), it always executed the first.
    // What is static variable?
    // Static variable makes the variables belong to the class
    // Static variable is sharable(changeable/common) for each object
    // Static variable can be accessed by other classes without creating an object
}
