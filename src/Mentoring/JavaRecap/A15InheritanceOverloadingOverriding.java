package Mentoring.JavaRecap;

public class A15InheritanceOverloadingOverriding {

    // Possible Interview Questions
    // What is overloading and overriding? (METHOD SIGNATURE)
    // Overloading and Overriding are really useful for the classes.
    // For example;
    // Overloading must have different method signature like same name with different parameters.
    // Overloading must be in same class or subclasses
    // Overloading methods can have static and final keyword
    // Overriding must have same method signature like same name with same parameters
    // Overriding must be in different class(parent-child classes)
    // Overriding methods cannot have static and final keyword
    // How can make the parent-child relationship?
    // with the 'extends' keyword
    // How can you make connections between classes?
    // What is inheritance?
    // Inheritance is the way of making a connection between classes
}
